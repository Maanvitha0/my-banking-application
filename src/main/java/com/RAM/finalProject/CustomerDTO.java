package com.RAM.finalProject;

public class CustomerDTO {
    private Long id;          // Unique identifier for the customer
    private String name;      // Name of the customer
    private String address;   // Address of the customer
    private String phone;     // Phone number of the customer
    private String email;     // Email address of the customer

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
